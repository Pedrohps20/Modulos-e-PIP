import math

# 1 - Acessar o número Pi
print(math.pi)
print(f"{math.pi:.2f}")

# 2 - Acessar numero de Euler
print(math.e)
print(f"{math.e:.2f}")

# 3 - Arredondando números
num1 = 10.4
print(math.ceil(num1))
print(math.floor(num1))

# 4 - Fatorial de um número
num2 = 7
print(math.factorial(num2))

# 5 - Potência de números
print(math.pow(2,5))

# 6 - raíz de quadrada
print(math.sqrt(9))

# 7 - MDC
mdc = math.gcd(20, 70) # maior divisor comum 
print(mdc)

# 8 - logarítmo
print(math.log(10))